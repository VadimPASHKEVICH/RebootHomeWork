package ООП_Полиморфизм;
/** Полиморфизм - это возможность применения одноименных методов с одинаковыми или различными наборами параметров
 * в одном классе или в группе классов, связанных отношением наследования. Благодаря полиморфизму нет надобности
 * писать отдельные методы (void) для каждого класса*/
public class Animal2 {
    public void eat(){
        System.out.println("Animal is eating");
    }
}
